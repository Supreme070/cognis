import { chromium } from 'playwright';
const BASE = 'https://www.cognis.group';
const arg = process.argv[2] || '/blog/';
const target = arg.startsWith('http') ? arg : BASE + arg;
const browser = await chromium.launch();
const ctx = await browser.newContext({ viewport: { width: 1440, height: 900 } });
const page = await ctx.newPage();
const responses = [];
const failed = [];
page.on('response', r => { responses.push({ u: r.url(), s: r.status() }); });
page.on('requestfailed', r => failed.push({ u: r.url(), e: r.failure()?.errorText }));
page.on('console', m => { if (m.type() === 'error') console.log('CONSOLE-ERR:', m.text().slice(0,200)); });
page.on('pageerror', e => console.log('PAGEERR:', String(e).slice(0,300)));
await page.goto(target, { waitUntil: 'networkidle', timeout: 60000 }).catch(e => console.log('GOTO:', e.message));
await page.waitForTimeout(3000);
// inspect blog cards
const info = await page.evaluate(() => {
  const out = {};
  out.title = document.title;
  out.bodyScrollH = document.body.scrollHeight;
  out.h1s = [...document.querySelectorAll('h1,h2')].slice(0,8).map(h => ({t:h.textContent.trim().slice(0,40), pos: getComputedStyle(h).position, top: h.getBoundingClientRect().top|0, left: h.getBoundingClientRect().left|0}));
  out.links = [...document.querySelectorAll('a[href*="/blog/"]')].map(a=>a.getAttribute('href')).filter((v,i,s)=>s.indexOf(v)===i).slice(0,20);
  out.imgCount = document.querySelectorAll('img').length;
  out.brokenImgs = [...document.querySelectorAll('img')].filter(i=>i.complete && i.naturalWidth===0).length;
  return out;
});
console.log('PAGEINFO:', JSON.stringify(info, null, 2));
const failMjs = failed.filter(f => /script_main|mjs/.test(f.u));
const failImg = failed.filter(f => /images\//.test(f.u));
console.log('FAILED script/mjs:', failMjs.length, failMjs.slice(0,3).map(f=>f.e));
console.log('FAILED images:', failImg.length);
console.log('mjs response statuses:', responses.filter(r=>/mjs$/.test(r.u.split('?')[0])).map(r=>r.s).join(','));
await page.screenshot({ path: 'test-results/site-audit/evidence/e2e-blog-journeys/networkidle-blog.png', fullPage: false });
await browser.close();
